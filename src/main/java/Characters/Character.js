import { Direction } from "./Direction.js";
// ==================================================
// Character(抽象クラス)
//
// 全キャラクター(主人公・敵)の共通の基底クラス
// ==================================================
export class Character {

    // ==================================================
    // コンストラクタ
    // ==================================================
    // 速さのデフォルトを1にして、主人公の呼び出しとも互換性を持たせる
    constructor(x, y, speed = 1) {

        // 抽象クラスなので、直接インスタンス化されるのを防ぐ
        if (new.target === Character) {
            throw new TypeError("Character は抽象クラスのため直接インスタンス化できません。");
        }

        // 現在のX座標(左右位置)
        this.x = x;

        // 現在のY座標(上下位置)
        this.y = y;

        // 移動速度
        this.speed = speed;

        // キャラクターが現在向いている方向(初期状態は停止)
        this.direction = Direction.NONE;
    }

    // ==================================================
    // 動き
    // ==================================================
    // キャラクターを移動させる処理(サブクラスで必ずオーバーライドする)
    move(map) {
        throw new Error("move() はサブクラスで実装してください。");
    }

    // ==================================================
    // getter
    // ==================================================
    // 子クラスからx座標の情報を取得
    getX() {
        return this.x;
    }

    // 子クラスからy座標の情報を取得
    getY() {
        return this.y;
    }

    // 子クラスから速度の情報を取得
    getSpeed() {
        return this.speed;
    }

    // 子クラスから向いている方向の情報を取得
    getDirection() {
        return this.direction;
    }
}